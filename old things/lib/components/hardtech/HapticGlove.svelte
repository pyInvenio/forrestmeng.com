		<h1 class="text-xl">The Project</h1>
		<p>
			I built this haptic glove that can simulate touch in VR environments. The glove is mostly
			inspired from LucidVR Haptic Glove build, with some slight modifications to make it more
			comfortable for me. The glove is made of 3D printed parts, and uses an ESP32 microcontroller
			to control the servos which lock finger rotation.
		</p>
		<h1 class="text-xl">What I learned</h1>
		<p>
			In this quick build, I learned about how a driver is coded and can be used to integrate
			control from SteamVR into a microcontroller. I also was able to use my new 3D printer to make
			custom parts for the glove. I found that haptic gloves is still needing a lot of work, and
			that there is a lot of room for improvement, such as easier and more accessible ways to wear
			the glove, technology to simulate shear force, and having more immersive haptic illusion.
		</p>
