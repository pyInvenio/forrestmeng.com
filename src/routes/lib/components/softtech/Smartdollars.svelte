		<h1 class="text-xl">The Problem</h1>
		<p>
			Financial literacy is a problem in the United States. Only a third of Americans can pass a
			financial literacy test. Young adults are especially vulnerable to financial scams and
			predatory lending as they go into the workforce.
			<br /><br />
			Learning about finance is a difficult task, however. Many people are intimidated by the jargon
			and complexity of the subject. Additionally, the information on one topic can take a long time
			to learn, and it is difficult to retain all of the information.
			<br /><br />
			Because of this, I built Smart Dollars AI, a GPT powered financial literacy aid that can answer
			questions and offer action items to help users learn and manage their finances with just a few
			clicks.
		</p>
		<h1 class="text-xl">How I'm building it</h1>
		<p>
			The chatbot is built using the OpenAI GPT-3.5-turbo API, which is a LLM focused on providing
			chat-like responses. I am webscraping data from many different financial sources, including
			government websites and Investopedia, using the data to be embedded into the chatbot. This
			way, the chatbot can provide factual information to the user on a variety of financial topics.
			My plan is to have this chatbot be a part of a larger financial literacy platform, which will
			include a variety of other features, such as a OCR based form and receipt reader, a budgeting
			tool, and an automated financial advisor.
