		<h1 class="text-xl">The Inspiration</h1>
		<p>
			As a digital artist, I often find myself searching for reference images for my work, spending
			hours on search engines but still not finding what I need. I wanted to create a tool that
			would allow me to search for reference images quickly and efficiently, providing automated
			filters and classification of images based on my search terms. Talking with other artists, I
			found that this was a common problem, and that many visual illustrator artists struggle to
			find the perfect reference image for their work. Additionally, sharing and collaborating on
			reference boards is a commmon need for artists, but currently, there is no easy way to do
			this.
			<br />
			<br />
			Artscaper is a tool that solves these problems, allowing artists to search for reference images
			quickly and efficiently through AI query generation and latent space searching, and share and collaborate
			on reference boards with other artists. I'm currently in the Startup Hokies Incubator, hoping
            to grow Artscaper into the best place for artists to save and share references.
