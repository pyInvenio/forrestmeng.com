		<h1 class="text-xl">Project Summary</h1>
		<p>
			Messenger apps like Discord & Slack have "channels" where multiple parties can send hundreds
			of texts daily. This can get out of hand pretty quickly if you've been offline, and we've
			sometimes had to scroll through hundreds of messages to understand what we've missed. The
			issue is that there isn't a quick & accessible way to figure things out. Tal;dera is a Discord
			bot that provides an easy-to-read, semantically rich summary of a channel's Discord activity
			in a particular time range. The summary is split into parts, and each part has a
			timestamp/link to its original messages. Users can call Tal;dera to extract message history
			and generate summaries (tl;dr's) of previous message chunks, given parameters such as number
			of messages, start time/date, output format, and chunk sizes. Summaries are given in an
			organized, embedded format, and are linked to where the conversations took place in the chats.
			Even though this was a challenging project for us, we were happy with what we were able to get
			done, and believe that this project has potential for B2B SaaS applications to team managers,
			employees, and chat service providers. We hope to continue this project by running pilot tests
			with smaller communities so that they can give feedback on Tal;dera, the summarizer bot for
			missed activity in Discord channels.
		</p>
