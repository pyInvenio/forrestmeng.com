			Outdated firmware and operating systems in IoT devices pose a significant cybersecurity threat
			to OT manufacturers' large scale actuators. Edge cybersecurity solutions are still in their
			infancy, and current firmware update methods are often unscalable, leading to devices not
			being updated for over 15 years. <br><br> Our research aims to address this issue through
			decentralization, ensuring manufacturers' systems are up-to-date and patchable by trusted
			entities. This approach eliminates attack surfaces without sacrificing connectivity or
			efficiency, making it a viable solution for securing large scale actuator IoT devices. Such
			devices often contain PLCs, RTUs, and computers that could be hijacked as part of botnets,
			posing financial losses and safety risks for operations and human workers.
